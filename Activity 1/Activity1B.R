num1 = 56
num2 = 45

less_than <- num1 < num2
paste("Less than: ", num1, " < ", num2, " = ", less_than)

less_than_or_equal_to <- num1 <= num2
paste("Less than or equal to: ", num1, " <= ", num2, " = ", less_than_or_equal_to)

greater_than <- num1 > num2
paste("Greater than: ", num1, " > ", num2, " = ", greater_than)

greater_than_or_equal_to <- num1 >= num2
paste("Greater than or equal to: ", num1, " >= ", num2, " = ", greater_than_or_equal_to)

equal_to <- num1 == num2
paste("Equal to: ", num1, " == ", num2, " = ", equal_to)

not_equal_to <- num1 != num2
paste("Not equal to: ", num1, " != ", num2, " = ", not_equal_to)
